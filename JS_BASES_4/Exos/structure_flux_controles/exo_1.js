// Utilisez une instruction "if" pour vérifier si une variable "age" est supérieure à 18.
// Si c'est le cas, affichez "majeur" à l'aide de "console.log()",
// sinon affichez "mineur"
let age = 19;
let textAffiche = 'Vous êtes';
